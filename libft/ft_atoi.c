int	ft_atoi(const char *str)
{
	int		i;
	long	sum;
	int		sign;

	i = 0;
	sum = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	sign = (str[i] == '-') ? -1 : 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		if ((((sum * 10) + (str[i] - 48)) - (str[i] - 48)) / 10 != sum)
			return ((sign == -1) ? 0 : -1);
		sum = (sum * 10) + (str[i] - 48);
		i++;
	}
	return (sum * sign);
}
