#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 10 && n > -10)
	{
		if (n < 0)
		{
			ft_putchar('-');
			ft_putchar(n * -1 + '0');
		}
		else
			ft_putchar(n + '0');
		return ;
	}
	ft_putnbr(n / 10);
	if (n < 0)
		ft_putchar((n % 10) * -1 + '0');
	else
		ft_putchar((n % 10) + '0');
}
